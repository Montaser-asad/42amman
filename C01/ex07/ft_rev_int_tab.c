void	ft_rev_int_tab(int *tab, int size)
{
	int	s;
	int	e;
	int	m;
	int	t;

	s = 0;
	e = size - 1;
	m = size / 2;
	while (s < m)
	{
		t = tab[s];
		tab[s] = tab[e];
		tab[e] = t;
		s++;
		e--;
	}
}
