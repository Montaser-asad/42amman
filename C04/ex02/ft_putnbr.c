#include<unistd.h>

void	ft_putnbr(int nb)
{
	char	x;
	int		sign;

	sign = 1;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			nb = -nb;
			write(1, "-", 1);
		}
		if (nb > 0)
			ft_putnbr (nb / 10);
		x = (nb % 10) + '0';
		write (1, &x, 1);
	}
}
