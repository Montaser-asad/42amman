#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*p;
	size_t			i;

	i = 0;
	p = (unsigned char *)s;
	while (n--)
	{
		p[i] = (unsigned char)c;
		i++;
	}
	return (s);
}
