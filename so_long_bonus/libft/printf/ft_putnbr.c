#include "libftprintf.h"

void	ft_putnbr(long nb, int *cnt)
{
	char	x;

	(*cnt)++;
	if (nb < 0)
	{
		(*cnt)++;
		nb = -nb;
		write(1, "-", 1);
	}
	if (nb >= 10)
		ft_putnbr(nb / 10, cnt);
	x = (nb % 10) + '0';
	write(1, &x, 1);
}
