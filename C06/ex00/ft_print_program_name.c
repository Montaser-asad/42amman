#include<unistd.h>

void	print(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{	
		write (1, &c[i], 1);
		i++;
	}
	write (1, "\n", 1);
}

int	main(int argc, char *argv[])
{
	if (argc == 1)
		print (argv[0]);
	return (0);
}
