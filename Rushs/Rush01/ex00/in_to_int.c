int	*in_to_int(int ac, char *av[], int input[16])
{
	int	i;
	int	j;

	i = -1;
	j = 0;
	while(i++ < 31)
	{
		if(av[1][i] != '\0' && av[1][i] != ' ')
		{
			input[j] = av[1][i] - '0';
			j++;
		}
	}
	return(input);
}
